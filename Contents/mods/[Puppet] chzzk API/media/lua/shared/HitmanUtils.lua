function HitmanUtils.findPoint(Ax, Ay, Bx, By, X)
    -- Calculate the direction vector from A to B
    local directionX = Bx - Ax
    local directionY = By - Ay

    -- Calculate the length of the direction vector
    local length = math.sqrt(directionX ^ 2 + directionY ^ 2)

    -- Normalize the direction vector
    local unitX = directionX / length
    local unitY = directionY / length

    -- Scale the unit vector by X units
    local scaledX = unitX * X
    local scaledY = unitY * X

    -- Calculate the coordinates of point P
    local Px = Ax + scaledX
    local Py = Ay + scaledY

    return Px, Py
end

function HitmanUtils.Bresenham(x0, y0, x1, y1)
    local points = {}

    local dx = math.abs(x1 - x0)
    local dy = math.abs(y1 - y0)

    local sx = (x0 < x1) and 1 or -1
    local sy = (y0 < y1) and 1 or -1

    local err = dx - dy

    while true do
        table.insert(points, {x=x0, y=y0})

        if x0 == x1 and y0 == y1 then
            break
        end

        local e2 = 2 * err

        if e2 > -dy then
            err = err - dy
            x0 = x0 + sx
        end

        if e2 < dx then
            err = err + dx
            y0 = y0 + sy
        end
    end

    return points
end

function HitmanUtils.IsWater(square)
    local objects = square:getObjects()
    for i=0, objects:size()-1 do
        local object = objects:get(i)
        local properties = object:getProperties()
        if properties then
            local water = properties:Is(IsoFlagType.water)
            if water then
                return true
            end
        end
    end
    return false
end

function HitmanUtils.GetGroundType(square)
    local groundType = "generic"
    local objects = square:getObjects()
    for i=0, objects:size()-1 do
        local object = objects:get(i)
        if object then
            local sprite = object:getSprite()
            if sprite then
                local spriteName = sprite:getName()
                if spriteName then
                    if spriteName:embodies("street") then
                        groundType = "street"
                    elseif spriteName:embodies("blends_natural") then
                        groundType = "grass"
                    end
                end
            end
        end
    end
    return groundType
end

function HitmanUtils.ReplaceDrainable(item)
    local itemType = item:getFullType()
    local newItemType
    if itemType == "farming.WateredCan" then
