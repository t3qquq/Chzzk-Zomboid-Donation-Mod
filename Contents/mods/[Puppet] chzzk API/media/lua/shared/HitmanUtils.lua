function HitmanUtils.IsController(zombie)

    -- ZOMBIE/HITMAN BEHAVIOUR IS FULLY CLIENT CONTROLLED
    -- SO CLIENTS ARE MIRRORING ACTIONS FOR ZOMBIES
    -- NOW, WE WANT VISUAL MIRRORING BY ALL CLIENTS 
    -- BUT THE ACTUAL ACTION CONSEQUENCES TO HAPPEN ONCE

    local gamemode = getWorld():getGameMode()

    if gamemode ~= "Multiplayer" then return true end

    local zx = zombie:getX()
    local zy = zombie:getY()

    local bestDist = 10000
    local bestPlayerId
    local playerList = getOnlinePlayers()
    for i=0, playerList:size()-1 do
        local player = playerList:get(i)
        local px = player:getX()
        local py = player:getY()

        local dist = HitmanUtils.DistTo(zx, zy, px, py)
        if dist < bestDist then
            bestDist = dist
            bestPlayerId = HitmanUtils.GetCharacterID(player)
        end
    end
    return bestPlayerId == HitmanUtils.GetCharacterID(getSpecificPlayer(0))
end

function HitmanUtils.IsFacing(sx, sy, sa, tx, ty, tolerance)
    local dx = tx - sx
    local dy = ty - sy
    local angleToTarget = math.deg(math.atan2(dy, dx))
    local angleDiff = (angleToTarget - sa + 180) % 360 - 180
    -- print ("angle: " .. angleDiff)
    return math.abs(angleDiff) < tolerance
end

function HitmanUtils.IsInAngle(observer, targetX, targetY)
    local omega = observer:getDirectionAngle()
    local targer_delta_x = targetX - observer:getX()
    local targer_delta_y = targetY - observer:getY()
    local theta = 57.295779513 * math.atan(targer_delta_y / targer_delta_x)

    -- print ("omega:" .. omega)
    -- print ("theta:" .. theta)

    if math.abs(theta - omega) < 45 then 
        return true
    else
        return false
    end
end

function HitmanUtils.CalcAngle (x1, y1, x2, y2)

    -- Calculate the difference in coordinates
    local dx = x2 - x1
    local dy = y2 - y1

    -- Use atan2 to get the angle in radians
    local angleRadians = math.atan2(dy, dx)

    -- Convert radians to degrees
    local angleDegrees = math.deg(angleRadians)

    -- Ensure the angle is within the range -180 to 180
    return angleDegrees
end

function HitmanUtils.AreEnemies(brain1, brain2)
    if  not brain1 or -- zombie case
        not brain2 or -- zombie case
        (brain1.clan ~= brain2.clan and (brain1.hostile or brain2.hostile)) or -- at least one of them is hostile and they are from different clans
        (brain1.loyal and brain2.hostileP) or -- is hostile against player only, but the second one is loyal to the player (babe case)
        (brain2.loyal and brain1.hostileP) then
            return true
    end
    return false
end

function HitmanUtils.ModifyWeapon(weapon, brain)
    local sight = brain.accuracyBoost
    local scopeItem
    if sight >= 1 and sight <= 2 then
        scopeItem = HitmanCompatibility.InstanceItem("Base.x2Scope")
    elseif sight > 2 and sight <= 3 then
        scopeItem = HitmanCompatibility.InstanceItem("Base.x4Scope")
    elseif sight > 3 then
        scopeItem = HitmanCompatibility.InstanceItem("Base.x8Scope")
    end

    if scopeItem then
        local mountList = scopeItem:getMountOn()
        for i=1, mountList:size() do
            local mount = mountList:get(i-1)
            if mount == weapon:getFullType() then
                weapon:attachWeaponPart(scopeItem)
            end
        end
    end
    return weapon
end

function HitmanUtils.GetClosestPlayerLocation(character, config)
    local result = {}
    result.dist = math.huge
    result.x = false
    result.y = false
    result.z = false
    result.id = false

    local mustSee = config.mustSee or true
    local hearDist = config.hearDist or 7

    local cx, cy = character:getX(), character:getY()
    local playerList = HitmanPlayer.GetPlayers()

    for i=0, playerList:size()-1 do
        local player = playerList:get(i)
        if player and not HitmanPlayer.IsGhost(player) then
            local px, py = player:getX(), player:getY()
            local dist = HitmanUtils.DistTo(cx, cy, px, py)
            if dist < result.dist and (not mustSee or (character:CanSee(player) or dist < hearDist)) then
                result.dist = dist
                result.x = player:getX()
                result.y = player:getY()
                result.z = player:getZ()
                result.d = player:getDirectionAngle()
                result.id = HitmanUtils.GetCharacterID(player)
            end
        end
    end
    return result
end

function HitmanUtils.GetClosestZombieLocation(character)
    local result = {}
    result.dist = math.huge
    result.x = false
    result.y = false
    result.z = false
    result.id = false

    local cx, cy = character:getX(), character:getY()

    local zombieList = HitmanZombie.CacheLightZ
    for id, zombie in pairs(zombieList) do
        local dist = math.sqrt(((cx - zombie.x) * (cx - zombie.x)) + ((cy - zombie.y) * (cy - zombie.y)))
        if dist < result.dist then
            result.dist = dist
            result.x = zombie.x
            result.y = zombie.y
            result.z = zombie.z
            result.id = zombie.id
        end
    end

    return result
end

function HitmanUtils.GetClosestHitmanLocation(character)
    local result = {}
    local cid = HitmanUtils.GetCharacterID(character)

    result.dist = math.huge
    result.x = false
    result.y = false
    result.z = false
    result.id = false

    local cx, cy = character:getX(), character:getY()

    local zombieList = HitmanZombie.CacheLightB
    for id, zombie in pairs(zombieList) do
        local dist = math.sqrt(((cx - zombie.x) * (cx - zombie.x)) + ((cy - zombie.y) * (cy - zombie.y)))
        if dist < result.dist and cid ~= id then
            result.dist = dist
            result.x = zombie.x
            result.y = zombie.y
            result.z = zombie.z
            result.d = zombie.d
            result.id = zombie.id
        end
    end

    return result
end

