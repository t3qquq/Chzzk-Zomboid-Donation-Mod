    result.y = false
    result.z = false
    result.id = false

    local cx, cy = character:getX(), character:getY()

    local hitmanList = HitmanZombie.CacheLightB
    if instanceof(character, "IsoZombie") then
        local brain = HitmanBrain.Get(character)
        for id, otherHitman in pairs(hitmanList) do
            if HitmanUtils.AreEnemies(brain, otherHitman.brain) then
            -- if brain.clan ~= otherHitman.brain.clan and (brain.hostile or otherHitman.brain.hostile) then
                local dist = math.sqrt(((cx - otherHitman.x) * (cx - otherHitman.x)) + ((cy - otherHitman.y) * (cy - otherHitman.y)))
                if dist < result.dist then
                    result.dist = dist
                    result.x = otherHitman.x
                    result.y = otherHitman.y
                    result.z = otherHitman.z
                    result.id = otherHitman.id
                end
            end
        end
    elseif instanceof(character, "IsoPlayer") then
        for id, otherHitman in pairs(hitmanList) do
            if otherHitman.brain.hostile or otherHitman.brain.hostileP then
                local dist = math.sqrt(((cx - otherHitman.x) * (cx - otherHitman.x)) + ((cy - otherHitman.y) * (cy - otherHitman.y)))
